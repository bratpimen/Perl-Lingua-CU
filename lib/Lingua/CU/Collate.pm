sub new {
	my $class = shift;
	my %tailoring = @_;

	while (my ($k, $v) = each %cu) {
		if (!exists $tailoring{$k}) {
			$tailoring{$k} = $v;
		} elsif ($k eq "entry") {
			$tailoring{$k} = $v . $tailoring{$k};
		} else {
			Carp::croak (__PACKAGE__ . "::new - Error: $k is reserved and cannot be overwritten");
		}
	}

	return new Unicode::Collate(%tailoring);
}

1;

